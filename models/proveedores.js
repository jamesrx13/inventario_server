const { Schema, model } = require('mongoose');

const UserSchema = Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    phone: {
        type: String,
        required: true,
        unique: true,
    },
    registerId: {
        type: String,
        required: true,
    },
    status: {
        type: Boolean,
        default: true,
    },
});

// Modificar metodos ya establecidos.
UserSchema.method('toJSON', function () {
    const { __v, _id, ...object } = this.toObject();
    object.uid = _id;
    return object;
});

module.exports = model('Proveedor', UserSchema);